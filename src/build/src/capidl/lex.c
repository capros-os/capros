  lx->current_file = 0;
  lx->current_basename = 0;
  lx->current_line = 1;
  lx->num_errors = 0;
  lx->curlyDepth = 0;
  lx->current_doc_comment = 0;
  lx->isActiveUOC = isActiveUOC;

  yyrestart(fin);
  yy_init = 1;		/* whether we need to initialize */
  yy_start = 0;	/* start state number */
