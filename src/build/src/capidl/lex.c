InternedString
mylexer_grab_doc_comment(MyLexer *lexer)
{
  if (lexer->current_doc_comment) {
    char *s;
    const char *is;
    s = buffer_asString(lexer->current_doc_comment);
    buffer_destroy(lexer->current_doc_comment);
    lexer->current_doc_comment = 0;
    is = intern(s);
    free(s);
    return is;
  }
  else {
    return 0;
  }
}

